package com.example.mohit.passingobjectbtwactivity93;

public class Student {
    String name,desig,number;

    public Student(String name, String desig, String number) {
        this.name = name;
        this.desig = desig;
        this.number = number;
    }

    public String getName() {
        return name;
    }

    public String getDesig() {
        return desig;
    }

    public String getNumber() {
        return number;
    }
}
